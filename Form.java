    void Update_table() {
        int column;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:33062/users", "root", "");
            String query = "SELECT * FROM employee_info";
            PreparedStatement add = con1.prepareStatement(query);
            ResultSet r = add.executeQuery();
            ResultSetMetaData Rss = r.getMetaData();
            column = Rss.getColumnCount();

            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
            table.setRowCount(0);

            while (r.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= column; i++) {
                    v2.add(r.getString("ID"));
                    v2.add(r.getString("Name"));
                    v2.add(r.getString("Gender"));
                    v2.add(r.getString("Position"));
                    v2.add(r.getString("Salary"));
                    v2.add(r.getString("Date"));
                    v2.add(r.getString("Address"));
                    v2.add(r.getString("Martial_Status"));
                    v2.add(r.getString("Image"));
                }
                table.addRow(v2);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }   
    }      
