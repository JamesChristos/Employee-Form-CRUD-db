    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        try {
            String id = model.getValueAt(selectedIndex, 0).toString();
            String name = txtName.getText();
            String gender = "";
            if (jradioMale.isSelected()) {
                gender = "Male";
            } else if (jradioFemale.isSelected()) {
                gender = "Female";
            }

            String position = jcbPos.getSelectedItem().toString();
            String salary = txtSalary.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String date = sdf.format(jDate.getDate());
            String address = txtAddress.getText();
            String status = jcbStatus.getSelectedItem().toString();
            String image = txt_IMG.getText();

            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:33062/users", "root", "");
            PreparedStatement insert = con1.prepareStatement("UPDATE employee_info SET Name=?, Gender=?, Position=?, Salary=?, Date=?, Address=?, Martial_Status=?, Image=? WHERE ID=?");
            insert.setString(1, name);
            insert.setString(2, gender);
            insert.setString(3, position);
            insert.setString(4, salary);
            insert.setString(5, date);
            insert.setString(6, address);
            insert.setString(7, status);
            insert.setString(8, image);
            insert.setString(9, id);

            insert.executeUpdate();

            JOptionPane.showMessageDialog(this, "Record Updated");

            clearingText();
            Update_table();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

