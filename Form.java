    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        txtID.setText(model.getValueAt(selectedIndex, 0).toString());
        txtName.setText(model.getValueAt(selectedIndex, 1).toString());
        String gender = model.getValueAt(selectedIndex, 2).toString();
        if (gender.equals("Male")) {
            jradioMale.setSelected(true);
        } else {
            jradioFemale.setSelected(true);
        }
        jcbPos.setSelectedItem(model.getValueAt(selectedIndex, 3).toString());
        txtSalary.setText(model.getValueAt(selectedIndex, 4).toString());
        Date date;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(selectedIndex, 5));
            jDate.setDate(date);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        txtAddress.setText(model.getValueAt(selectedIndex, 6).toString());
        jcbStatus.setSelectedItem(model.getValueAt(selectedIndex, 7).toString());
        ImageIcon imageIcon = new ImageIcon((String) model.getValueAt(selectedIndex, 8));
        Image image = imageIcon.getImage();
        Image scaledImage = image.getScaledInstance(labelImg.getWidth(), labelImg.getHeight(), Image.SCALE_SMOOTH);
        labelImg.setIcon(new ImageIcon(scaledImage));
    }//GEN-LAST:event_jTable1MouseClicked

